P = list(range(3, 18+1)) #1 способ
Q = [i for i in range(11, 24+1)] #2 способ
A = []# Изначально А ПУСТОЙ тк необходима наименьшая длина
for x in range(1, 25):
    if (((x in P) and (x in Q)) <= (x in A)) == 0: #приравниваем ко лжи - тогда будем добавлять А т.е. делать искомую истину
        A.append(x)
print(A)
print(A[-1] - A[0]) #Находим длину отрезка - из макс элемента вычитаем меньший, но проверяем, чтобы оба числа БЫЛИ в отрезках(иначе берем ближнее) а также не было РАЗРЫВОВ(оч редко)
